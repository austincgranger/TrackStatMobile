using Microsoft.EntityFrameworkCore;
using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using TrackStatMobile.Services;

namespace TrackStatMobile.Models
{
    public class Repo : DbContext, IUser<User>, IStats<Stats>
    {
        /// <summary>
        /// Creates our Repository
        /// </summary>
        /// <param name="dbPath">the platform specific path to the database</param>
        public Repo(string dbPath) : base()
        {
            _dbPath = dbPath;

            Database.EnsureCreated();
            Database.Migrate();
        }

        string _dbPath;
        public DbSet<User> Users { get; set; }
        public DbSet<Stats> Stats { get; set; }
        protected override void OnConfiguring(DbContextOptionsBuilder optionsBuilder)
        {
            Debug.WriteLine("**** OnConfiguring");
            optionsBuilder.UseSqlite($"Filename={_dbPath}");
        }
        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            Debug.WriteLine("**** OnModelCreating");
            // Setting Id as PrimaryKey
            modelBuilder.Entity<User>()
                .HasKey(p => p.UserId);

            // Require Text to be set

            //modelBuilder.Entity<Item>()
            //    .Property(p => p.Text)
            //    .IsRequired();

            // As an example in case any of you need to see how

            // Added initialization data
#if DEBUG
            modelBuilder.Entity<User>()
                .HasData(
                    new User { UserId = Guid.NewGuid().ToString(), Username = "First user" },
                    new User { UserId = Guid.NewGuid().ToString(), Username = "Second user" },
                    new User { UserId = Guid.NewGuid().ToString(), Username = "Third user" },
                    new User { UserId = Guid.NewGuid().ToString(), Username = "Fourth user" }
                );
#endif
        }

        #region IDataStore<User> start
        public async Task<User> GetUserAsync(string id) // BY ID
        {
            var user = await Users.FirstOrDefaultAsync(u => u.UserId == id).ConfigureAwait(false);
            return user;
        }

        public async Task<IEnumerable<User>> GetAllUsersAsync(bool forceRefresh = false)
        {
            // TODO: forceRefresh
            var allUsers = await Users.ToListAsync().ConfigureAwait(false);
            return allUsers;
        }

        public async Task<bool> AddUserAsync(User user)
        {

            user.UserId = Guid.NewGuid().ToString(); // Id is of type string but is generated by a GUID
            await Users.AddAsync(user);
            await SaveChangesAsync();

            return true;
        }

        public async Task<bool> UpdateUserAsync(User user)
        {
            Users.Update(user);
            await SaveChangesAsync().ConfigureAwait(false);
            // No error handling yet
            return true;
        }

        public async Task<bool> DeleteUserAsync(string id)
        {
            var userRemove = Users.FirstOrDefault(x => x.UserId == id);
            if (userRemove != null)
            {
                Users.Remove(userRemove);
                await SaveChangesAsync().ConfigureAwait(false);
            }

            return true;
        }
        #endregion

        #region IDataStore<Stats> start
        public async Task<Stats> GetStatsAsync(string id) // BY ID
        {
            var stats = await Stats.FirstOrDefaultAsync(u => u.StatisticId == id).ConfigureAwait(false);
            return stats;
        }

        public async Task<IEnumerable<Stats>> GetAllStatsAsync(bool forceRefresh = false)
        {
            // TODO: forceRefresh
            var stats = await Stats.ToListAsync().ConfigureAwait(false);
            return stats;
        }

        public async Task<bool> AddStatsAsync(Stats stats)
        {

            stats.StatisticId = Guid.NewGuid().ToString(); // Id is of type string but is generated by a GUID
            await Stats.AddAsync(stats);
            await SaveChangesAsync();

            return true;
        }

        public async Task<bool> UpdateStatsAsync(Stats stats)
        {
            Stats.Update(stats);
            await SaveChangesAsync().ConfigureAwait(false);
            // No error handling yet
            return true;
        }

        public async Task<bool> DeleteStatsAsync(string id)
        {
            var statR = Stats.FirstOrDefault(x => x.StatisticId == id);
            if (statR != null)
            {
                Stats.Remove(statR);
                await SaveChangesAsync().ConfigureAwait(false);
            }

            return true;
        }
        #endregion
    }
}
